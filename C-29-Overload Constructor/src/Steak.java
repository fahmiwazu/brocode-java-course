
public class Steak {
	
	String daging;
	String saus;
	String Kentunk;
	String matank;
	
	Steak(String daging, String saus, String Kentunk, String matank){
		
		this.daging = daging;
		this.saus = saus;
		this.Kentunk = Kentunk;
		this.matank = matank;
	}
	
	Steak(String daging, String saus, String matank){
		
		this.daging = daging;
		this.saus = saus;
		this.matank = matank;
	}
	
	Steak(String daging, String saus){
		
		this.daging = daging;
		this.saus = saus;

	}
	
	Steak(String daging){
		
		this.daging = daging;


	}
	
	
}
